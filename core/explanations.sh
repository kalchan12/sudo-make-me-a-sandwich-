_explain_by_index() {
    local -n list="$1"
    local idx="$2"
    local i=1
    for info in "${list[@]}"; do
        if [ "$i" -eq "$idx" ]; then
            local name="${info%%|*}"
            _explain_tool "$name"
            return
        fi
        ((i++))
    done
    log_message "WARN" "Invalid tool index: $idx"
}
